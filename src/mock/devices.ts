export const getDeviceById = (id: string): Device | undefined => {
  return mockDevices.find(d => d.id === id);
};

export const getDeviceByNo = (deviceNo: string): Device | undefined => {
  return mockDevices.find(d => d.deviceNo === deviceNo);
};

export const getDeviceByQRCode = (qrCode: string): Device | undefined => {
  return mockDevices.find(d => d.qrCode === qrCode);
};

export const getDevicesByStatus = (status: Device['status']): Device[] => {
  return mockDevices.filter(d => d.status === status);
};

export const getDevicesByLocation = (location: string): Device[] => {
  return mockDevices.filter(d => d.location.includes(location));
};
